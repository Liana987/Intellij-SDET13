package com.syntax.class18;

public class Cat {

    String name;
    String breed;
    String color;
    int age;
    double weight;

    // How to create a Constructor 1) Right click 2) Generate 3) Constructor

    public Cat(String name, String breed, String color, int age, double weight) {
        this.name = name;
        this.breed = breed;
        this.color = color;
        this.age = age;
        this.weight = weight;
    }
}
